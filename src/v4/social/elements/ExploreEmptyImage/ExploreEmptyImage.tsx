interface ExploreEmptyImageProps {
  pageId?: string;
  componentId?: string;
}

export const ExploreEmptyImage = ({ pageId = '*', componentId = '*' }: ExploreEmptyImageProps) => {
  const elementId = 'explore_empty_image';
  const {
    currentTheme,
    accessibilityId,
    config,
    defaultConfig,
    isExcluded,
    uiReference,
    themeStyles,
  } = useAmityElement({
    pageId,
    componentId,
    elementId,
  });

  if (isExcluded) return null;

  return (
    <IconComponent
      defaultIconName={defaultConfig.icon}
      configIconName={config.icon}
      imgIcon={() => (
        <img
          style={themeStyles}
          src={config.icon}
          alt={uiReference}
          data-qa-anchor={accessibilityId}
        />
      )}
      defaultIcon={() => (
        <div data-qa-anchor={accessibilityId} style={themeStyles}>
          {currentTheme === 'light' ? <ExploreEmptyImageSvg /> : <DarkExploreEmptyImageSvg />}
        </div>
      )}
    />
  );
};
